import Foundation
import UIKit

extension UIFont {
    
    class func athelas(size: CGFloat) -> UIFont {
        return UIFont(name: "Athelas-Regular", size: size)!
    }
    
    class func hiragino(size: CGFloat) -> UIFont {
        return UIFont(name: "HiraginoSans-W3", size: size)!
    }
    
    class func hiraginoBold(size: CGFloat) -> UIFont {
        return UIFont(name: "HiraginoSans-W6", size: size)!
    }
    
}

